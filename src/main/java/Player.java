public class Player {

    private Card playerHand;
    private String name;

    public Player(String name) {
        this.playerHand = playerHand;
        this.name = name;
    }

    public Card getPlayerHand() {

        return this.playerHand;
    }

    public void setPlayerHand(Card playerHand) {
        this.playerHand = playerHand;
    }

    public String getName() {

        return name;
    }

}
